public class CharecterTracker
{
    int[][] charectersAndProperties = new int[][]

    void Update() {
        Player SendingPlayer = new Player()
        SendingPlayer.Player += playerInformation
        bool exists = playerInformation.Any(x=> x.Any(y=> y == playerInformation[2]));
        if (exists)
        {
        int[] result = charectersAndProperties.FirstOrDefault(x=>x.Any(y=>y == playerInformation[2]));    
        int pos = IndexOf(charectersAndProperties, result)
        charectersAndProperties[pos] = playerInformation
        } else {
        charectersAndProperties[charectersAndProperties.length + 1] = playerInformation
        }
    }
}